package com.rise;

public class Offer {

	private String offerName;
	private int price;
	
	public String getOfferName() {
		return offerName;
	}
	
	public void setOfferName(String offerName) {
		this.offerName = offerName;
	}
	
	public int getPrice() {
		return price;
	}
	
	public void setPrice(int price) {
		this.price = price;
	}
}
